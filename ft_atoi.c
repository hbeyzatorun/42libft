#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int		i;
	int		sign;
	size_t	num;

	i = 0;
	sign = 1;
	num = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == ' ')
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			sign *= -1;
		i++;
	}
	while ((nptr[i] >= '0' && nptr[i] <= '9'))
	{
		num *= 10;
		if (num >= 9223372036854775807)
			return (-1);
		num += nptr[i] - 48;
		i++;
	}
	return ((int)(sign * num));
}
