#include "libft.h"
#include <bsd/string.h>
#include <stdio.h>

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (dst[i] && i < size)
	{
		i++;
	}
	while (src[j] && (i + j + 1) < size)
	{
		dst[i + j] = src[j];
		j++;
	}
	if (i < size)
	{
		dst[i + j] = '\0';
	}
	while (src[j])
	{
		j++;
	}
	return (i + j);
}

int	main(void)
{
	char	dest[] = "aaa";
	char	src[] = "bbb";

	printf("%zu", strlcat(dest, src, 3));
}
