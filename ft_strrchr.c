char	*ft_strrchr(char *s, int c)
{
	char	a;
	char	*res;
	int		i;

	i = 0;
	a = c;
	res = 0;
	while (s[i])
	{
		if (s[i] == a)
		{
			res = &s[i];
		}
		i++;
	}
	return (res);
}
